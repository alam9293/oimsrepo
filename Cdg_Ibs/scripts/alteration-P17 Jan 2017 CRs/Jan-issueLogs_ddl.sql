COMMENT ON COLUMN ITTB_CP_MASTER_TAG_ACCT.CUST_NO IS 'Cust No';
COMMENT ON COLUMN ITTB_CP_MASTER_TAG_ACCT.ACCOUNT_NO IS 'Account No';
COMMENT ON COLUMN ITTB_CP_MASTER_TAG_ACCT.ACCOUNT_CATEGORY IS 'Account Category';

