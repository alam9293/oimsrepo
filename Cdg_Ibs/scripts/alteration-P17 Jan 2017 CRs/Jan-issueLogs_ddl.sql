CREATE TABLE ITTB_CP_MASTER_LOGIN (
	MASTER_LOGIN_NO NUMBER(8) NOT NULL,
	NAME VARCHAR2(80),
	EMAIL VARCHAR2(80) ,
	TELEPHONE VARCHAR2(80) ,
	COMPANY_NAME VARCHAR2(80) ,
	LOGIN_ID VARCHAR2(80),
	CREATED_BY VARCHAR2(40),
	CREATED_DT TIMESTAMP NOT NULL,
	UPDATED_BY VARCHAR2(40),
	UPDATED_DT TIMESTAMP
);

-- Create SEQUENCE
CREATE SEQUENCE  "ITSQ_MASTER_LOGIN_NO"  MINVALUE 1 MAXVALUE 99999999 INCREMENT BY 1 START WITH 2 CACHE 10 NOORDER  NOCYCLE ;

COMMENT ON COLUMN ITTB_CP_MASTER_LOGIN.MASTER_LOGIN_NO IS 'SeqNo';
COMMENT ON COLUMN ITTB_CP_MASTER_LOGIN.NAME IS 'Name';
COMMENT ON COLUMN ITTB_CP_MASTER_LOGIN.EMAIL IS 'Email Address';
COMMENT ON COLUMN ITTB_CP_MASTER_LOGIN.TELEPHONE IS 'Telephone';
COMMENT ON COLUMN ITTB_CP_MASTER_LOGIN.COMPANY_NAME IS 'Company Name';
COMMENT ON COLUMN ITTB_CP_MASTER_LOGIN.LOGIN_ID IS 'Login Id';

CREATE TABLE ITTB_CP_MASTER_TAG_ACCT (
	TAG_ACCOUNT_NO NUMBER(8) NOT NULL,
	MASTER_LOGIN_NO NUMBER(8) NOT NULL,
	CUST_NO VARCHAR2(6) ,
	ACCOUNT_NO NUMBER(8) ,
	ACCOUNT_NAME VARCHAR2(80),
	ACCOUNT_CATEGORY VARCHAR2(4),
	CREATED_BY VARCHAR2(40),
	CREATED_DT TIMESTAMP NOT NULL,
	UPDATED_BY VARCHAR2(40),
	UPDATED_DT TIMESTAMP
);

-- Create SEQUENCE
CREATE SEQUENCE  "ITSQ_TAG_ACCOUNT_NO"  MINVALUE 1 MAXVALUE 99999999 INCREMENT BY 1 START WITH 2 CACHE 10 NOORDER  NOCYCLE ;

COMMENT ON COLUMN ITTB_CP_MASTER_TAG_ACCT.TAG_ACCOUNT_NO IS 'Seq No';
COMMENT ON COLUMN ITTB_CP_MASTER_TAG_ACCT.MASTER_LOGIN_NO IS 'SeqNo of Ittb_Cp_Master_Login';
